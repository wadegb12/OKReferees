        <p><strong>Training</strong></p>
        <ol>
          <li><strong> Out-of-state Training</strong> <strong>- </strong>The SRC, using input from assessors and mentor referees, will encourage the promotion of referees to a higher-grade level, up to and including the PRO Badge. Referees who are designated as potential National referees will be given opportunities to attend out-of-state training seminars and tournaments which will give the candidate an opportunity to showcase his/her abilities. The SRC, within its budgetary ability, may underwrite attendance at such events.</li>
          <li><strong> Assessor and Instructor Advanced Training </strong><strong>&ndash; </strong>The SRC may provide budgetary assistance for those assessors and instructors who wish to attend advanced training within their discipline, e.g., PEACE courses, State Assessors courses.</li>
          <li><strong> USSF sponsored fitness Training </strong><strong>&ndash; </strong>Referees who attend USSF sponsored advanced training between July 1 and the regularly scheduled Oklahoma fitness exams, may not submit fitness exam results from that training without prior approval from the SRC or the Upgrade Panel.</li>
