        <p><strong>Clinics</strong></p>
        <ol>
          <li><strong> Entry-level clinics (Grade 9)</strong> - The entry-level course for referees in Oklahoma is the Grade 9 course. Clubs and/or leagues may request to host the clinics at locations in or near their sites. A referee course contract form explaining the requirements for hosting a clinic is available on the SRC website. Referees who wish to continue their growth in officiating to the Grade 8 can begin the upgrade process by attending a Grade 8 clinic.</li>
        </ol>
        <ol start="2">
          <li><strong> Grade 8 clinics</strong> &ndash; Referees may upgrade to the Grade 8 level in Oklahoma after successfully completing the Grade 9 entry-level clinic. A referee course contract form explaining the requirements for hosting a clinic is available on the SRC website. Referees who wish to continue their growth in officiating to the Grade 7 Basic level can begin the upgrade process. (See Upgrading Process)</li>
          <li><strong> Upgrade Clinics</strong>- Will be held by the SRC as necessary. The upgrade clinic will be posted with place, date, and time for all approved to attend upgrade clinic by the upgrade panel.</li>
          <li><strong> Recertification clinics</strong> &ndash; Every registered referee, instructor, assignor and assessor must attend a Recertification Clinic each year prior to registering with USSF as a referee, assignor, assessor, instructor for the following calendar year. Recertification Clinics will be set up by the SRC in cooperation with clubs and/or leagues. Each club and/or league may have one Recertification clinic a year at no charge. A fee will be charged for each additional clinic, see Fee Schedule.</li>
          <ol type=a>
            <li>A Recertification Clinic for grades 7 basic, 7 advanced, grade 6, grade 5, and grade 5AR referees will be held the first Saturday in August of each year. The clinic will be held in conjunction with the annual fitness exam required of such referees.</li>
            <li>A Recertification Clinic for Assignors, Assessors, and Instructors will be held annually. It will be held on the next to the last Saturday in July.</li>
            <li>Failure to attend a Recertification Clinic will prevent a referee, instructor, assessor and/or, assignor from registering for the following calendar year.</li>
            <li>Reference materials will be distributed to Recertified officials at the Recertification Clinic (if available).</li>
          </ol>
        </ol>
