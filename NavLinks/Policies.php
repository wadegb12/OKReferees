        <p><strong>SECTION I: Objectives</strong></p>
        <p>The Oklahoma State Referee Committee (SRC) is dedicated to all soccer referees, whose purpose is to ensure the development of the United States Soccer Referee in terms of quality and quantity through achieving excellence in governance, education, and administration. The program shall work in the interests of all its members.</p>
        <br>
        <p><strong>SECTION II: Structure</strong></p>
        <ol>
          <li>The Chair of the Committee shall be the State Referee Chairman</li>
          <li>The membership of the SRC shall consist of:</li>
          <ol type=a>
            <li>State Referee Chairman</li>
            <li>State Referee Administrator (SRA)</li>
            <li>State Youth Referee Administrator (SYRA)</li>
            <li>State Director of Assessment (SDA)</li>
            <li>State Director of Instruction (SDI)</li>
            <li>State Director of Assignors</li>
            <li>the President or a representative of the Youth State Association.</li>
            <li>the President or representative of the Adult State Association.</li>
          </ol>
          <li>The SRA serves a two (2) year term by appointment of the Adult and Youth Board Presidents.</li>
          <li>The SYRA serves a two (2) year term by appointment of the Youth Board President with recommendation by the SRA.</li>
          <li>The State Referee Chairman shall be elected according to the bylaws of the Oklahoma Soccer Association.</li>
          <li>The remaining committee members serve by appointment of the SRA.</li>
        </ol>
        <br>
        <p><strong>SECTION III: Duties and Responsibilities</strong></p>
        <ol>
          <li>The SRC shall develop a budget to cover its projected income and expenses for its fiscal year as established by the Oklahoma Soccer Association bylaws.</li>
          <li>The SRC shall organize and present certification and recertification clinics for the referees, assessors, assignors, and instructors in the state of Oklahoma.</li>
          <li>The SRC shall organize and present an upgrade clinic for the referees who wish to attain higher levels of referee proficiency.</li>
          <li>The SRC shall organize and present an annual fitness exam and recertification clinic for all referees that are grade 7 and higher.</li>
          <li>The SRC shall select a Youth Referee of the Year (Aaron Whitewolf Award) and a Referee of the Year based on nominations.</li>
          <li>The SRC will oversee the registration of referees, assignors, instructor and assessors within the state of Oklahoma.</li>
          <li>The SRC shall select the referees and assessors who will represent Oklahoma at the Region III tournaments.</li>
          <li>The SRC shall provide training for new referees, assessors, assignors, and instructors as the need arises.</li>
          <li>The SRC will provide entry-level clinics for assessors, instructors, and assignors on an as needed basis. The SRC will determine who shall be invited to participate in these clinics based upon their referee grade, ability, and experience.</li>
          <li>The SRC shall encourage qualified referees to achieve a higher referee grade level, up to and including the Professional Referee Organization.</li>
          <li>The SRC shall meet on the first Wednesday of every month, unless The State Referee Chairman chooses to change it with 7-day written notice to the SRC.</li>
