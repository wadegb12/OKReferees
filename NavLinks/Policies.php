        <p><strong>Finance</strong></p>
        <ol>
          <li><strong> Authorization of Payments from the Referee Fund </strong>- To ensure any funds spent will be reimbursed from the Referee Budget, pre-approval should be obtained from the appropriate section head as indicated below:</li>
          <ol type=a>
            <li>The State Director of Assessment will authorize payments to those certified assessors who are assigned to assess a referee, as requested by the referee.</li>
            <li>The State Director of Instruction will authorize payments to those certified instructors who are assigned to instruct a referee course.</li>
            <li>The State Youth Referee Administrator is authorized to expend funds for Youth Regionals in an amount not to exceed the funding provided by the SRC for the event.</li>
            <li>The State Referee Administrator will authorize all other expenses from the referee fund.</li>
          </ol>
          <li><strong> Reimbursement for Training, Clinics, or Tournaments </strong><strong>&ndash; </strong>Based on the criteria and considerations listed below, the SRC will partially reimburse certain expenses incurred by registered referees in Oklahoma traveling to USSF sanctioned clinics and tournaments outside of the state. The SRC makes no guarantee that any applicant will receive remuneration in any form. Each application will be considered individually.</li>
        <div><strong>Criteria and considerations are as follows:</strong></div>
          <ol type=a>
            <li>The applicant must be a currently registered USSF referee, registered in Oklahoma.</li>
            <li>The applicant must have obtained a referee grade of 6 or higher. Grade 7 Advanced referees who have stated in writing their intention to upgrade will also be considered.</li>
            <li>The applicant must apply for funding two months prior to the scheduled start date of the clinic or tournament. A letter stating the name and location of the clinic or tournament, anticipated total expenses and why the applicant believes this would further his/her referee career must be sent to the SRA or SYRA.</li>
            <li>The tournament and/or clinic must be sanctioned by the USSF.</li>
            <li>Funding for this program is limited and may not be available yearly. Applicants should not assume that they will be reimbursed for any expenditure unless notified in writing, by the SRC. The applicant will be informed as to the amount of the stipend approved by the SRC.</li>
            <li>Funding after the fact will not be considered. All applications must be approved in advance.</li>
            <li>If approved, only partial funding will be provided. The SRC believes that individual referees must have a financial stake in their development and advancement.</li>
          </ol>
        </ol>
