        <p><strong>Fitness Requirements</strong></p>
        <ol>
        <li><strong> Fitness exam</strong> &ndash; A yearly fitness exam will be held the first Saturday of August as a part of Recertification for referee grades 7 advanced, grade 6, grade 5 and grade 5AR. This fitness exam is also offered to any referee desiring to upgrade from a grade 8 to a 7 advanced, or grade 7A to grade 6, or grade 6 to grade 5. The fitness exam will be administered per the Oklahoma Upgrading Process. If a referee fails to attend the fitness exam, the referee will be provided the opportunity for a make-up fitness exam at a fee (See Fee Schedule). Failure to complete the fitness exam will cause a referee to be downgraded to a 7 basic, assuming the referee has successfully completed a recertification course.</li>
        </ol>
        <ol start="2">
          <li><strong> Fitness Exam requirements- </strong>See Abilities and Requirements</li>
        </ol>
        <br>
        <p><strong>Assessment</strong></p>
        <ol>
          <li><strong> Assessments</strong>- Are field evaluations done by registered assessors. The purpose for assessments are to upgrade, maintain, or develop a higher level of referees.</li>
        </ol>
        <div class="a">
          <ul class="disc">
            <li>The cost for assessments will be determined by the level of referee needing the assessment. (see Fee Schedule)</li>
            <li>Assessments at USSF sponsored tournaments <strong>- </strong>When the SRC sends referees to USSF sponsored tournaments or events, any assessments obtained at such a venue <strong>may</strong> be considered by the State Director of Assessments.</li>
            <li>Collegiate Game Assessments <strong>&ndash; </strong>Effective January 1, 2018 collegiate assessments will be <strong>NOT</strong> counted for any USSF assessment. This policy is a year-to-year policy.</li>
            <li>If referee should fail assessment. The ratio for make-up assessment is 2:1, must pass two assessments for one failed assessment.</li>
            <li>If referee or assistant referee should fail three assessments. The referee or assistant referee may not continue anymore assessments until the next registration year.</li>
            <li>Assessment fees from Jan. 1 thru May 31 will be $75 and Assessment fees Jun 1 thru Dec 31 will be $100.</li>
            <li>Grade 7B ($50 Must be before June 30<sup>th</sup>)</li>
          </ul>
        </div>
